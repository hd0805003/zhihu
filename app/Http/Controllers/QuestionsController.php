     */
    public function store(StoreQuestionRequest $request)
    {
        $data=[
        'title'=>$request->title,
        'body'=>$request->body,
        'user_id'=>Auth::id()
        ];
        $question=Question::Create($data);
        return redirect()->route('question.show',[$question->id]);
    }

